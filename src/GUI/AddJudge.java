    private void saveButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveButtonMouseClicked
        String judgeName = name.getText();
        if(judgeName.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please enter a name");
        }
        else if(!selected){
            JOptionPane.showMessageDialog(rootPane, "Please select a gender");
        }
        else {
            name.setEditable(false);
            new_Judge = new Judge(judgeName,judgeGender);
            s.addJudge(new_Judge);   
            JOptionPane.showMessageDialog(rootPane, "Successfully created");
            name.setEditable(true);
            name.setText("");
            name.requestFocus();
            buttonGroup1.clearSelection();
        }
    }//GEN-LAST:event_saveButtonMouseClicked

