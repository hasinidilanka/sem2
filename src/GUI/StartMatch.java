    private void getWinnersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_getWinnersMouseClicked
        if(isStartClicked){
            final DefaultTableModel dtm2=(DefaultTableModel) tbl2.getModel();
            board.setWinners();
            board.setMatch(m);
                for(Swimmer s :board.getSwimmer() ){
                    for(int x = 0;x<dtm2.getRowCount();x++){
                        if(s.getIndexNumber()==(int)dtm2.getValueAt(x, 0)){
                            dtm2.setValueAt(s.getRank(), x, 3);
                        }
                    }
                }
                String Address = "/GUI/spectator_watch.jpg";
                for(Spectator s :sc.getAllSpectator()){
                      Address = board.notifySpectator(s);
                }
                for(Judge j :sc.getAllJudges()){
                      j.setResult(board.getSwimmer());
                }
                for(SupportingStaff ss:sc.getAllStaff()){
                      ss.setResult(board.getSwimmer());
                }
                spectators_icon1.setIcon(new javax.swing.ImageIcon(getClass().getResource(Address)));
                spectators_icon2.setIcon(new javax.swing.ImageIcon(getClass().getResource(Address)));
                spectators_icon3.setIcon(new javax.swing.ImageIcon(getClass().getResource(Address)));
                isWinnerClicked=true;
                sc.addMatch(m);
                board.clearScoreBorad();
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "First click the start button");
        }
        
        
        
    }//GEN-LAST:event_getWinnersMouseClicked

