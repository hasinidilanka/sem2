    private void SelectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SelectMouseClicked
        isSelectClicked=true;
        if(isGetSwimmerClicked){
            DefaultTableModel dtm1=(DefaultTableModel)tbl1.getModel();
            DefaultTableModel dtm=(DefaultTableModel) tbl.getModel();
            DefaultTableModel dtm2=(DefaultTableModel) tbl2.getModel();
            JLabel[] swimmerName = {swimmerName1,swimmerName2,swimmerName3,swimmerName4,swimmerName5};
            JSlider[] swimLane = {swimmer1,swimmer2,swimmer3,swimmer4,swimmer5};
            JLabel[] touchPad = {touchPad1,touchPad2,touchPad3,touchPad4,touchPad5};
            int number=tbl.getSelectedRow();      
            if(number!=-1 && selection<5){
                dtm1.setValueAt(tbl.getValueAt(number, 0), selection,0);
                dtm1.setValueAt(tbl.getValueAt(number, 1), selection,1);
                swimmerName[selection].setText("     "+(String)tbl.getValueAt(number, 1));
                dtm.removeRow(number);
                for(Swimmer s: swimmer){
                    if((Integer)dtm1.getValueAt(selection, 0)==s.getIndexNumber()){
                        selectedSwimmer.add(s);
                        s.setSwimLane(selection+1);
                        selectedSwimLanes.add(swimLane[selection]);
                        selectedtouchpad.add(touchPad[selection]);
                        dtm2.addRow(new Object[]{s.getIndexNumber(),s.getName()});
                    }
                }            
                selection++;            
            }
            
       }else{
            JOptionPane.showMessageDialog(rootPane,"First Click the getSwimmer button");
        }
  
        
        
      
    }//GEN-LAST:event_SelectMouseClicked

