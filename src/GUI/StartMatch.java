    private void getSwimmerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_getSwimmerMouseClicked
        isGetSwimmerClicked = true;
        DefaultTableModel dtm=(DefaultTableModel) tbl.getModel();       
        dtm.setRowCount(0);
        ageRange = age_range.getSelectedIndex();
        style = match_style.getSelectedIndex();
        select_Judge = add_Judge.getSelectedIndex();
         if(ageRange==0){
            JOptionPane.showMessageDialog(jPanel1, "Please select an age range");
        }if(style==0){
            JOptionPane.showMessageDialog(jPanel1, "Please select a style");
        }if(selected == false){
            JOptionPane.showMessageDialog(jPanel1, "Please select a gender");
        }if(select_Judge==0){
            JOptionPane.showMessageDialog(jPanel1, "Please select a judge");
        }else{                
                m = new Match(gender,style,ageRange);
                m.setJudge(judges.get(select_Judge-1));             
                swimmer = sc.getSwimmers(m);
                if(swimmer.size()!=0){
                    for(Swimmer s : swimmer){
                    dtm.addRow(new Object[]{s.getIndexNumber(),s.getName()});
                    } 
                }else{
                    JOptionPane.showMessageDialog(rootPane, "No swimmers");
                }
                    
                             
        }
        
    }//GEN-LAST:event_getSwimmerMouseClicked
    
   
