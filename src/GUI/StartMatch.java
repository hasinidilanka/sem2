    private void startMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_startMouseClicked
        if(isSelectClicked){
                
                final Random ran = new Random();
                final DefaultTableModel dtm2=(DefaultTableModel) tbl2.getModel();
                //final ArrayList<Integer> indexArray = new ArrayList<>() ;
                final ArrayList<Long> time = new ArrayList<>();
                ArrayList<Thread> threadArray = new ArrayList<>() ;
                
               
                for(final JSlider j : selectedSwimLanes){
                    Runnable r = new Runnable() {
                        final int index = selectedSwimLanes.indexOf(j);
                        public void run() {
                            long startTime = System.currentTimeMillis(); 
                            while(true){
                                try {
                                    //.setStyle(m.getMatchstyle());                                   
                                    j.setValue(j.getValue()+selectedSwimmer.get(index).swim(m.getMatchstyle()));
                                    //j.setValue(j.getValue()+Location.getLocation(selectedSwimmer.get(index)));
                                    if(j.getValue()==100){
                                        long endTime = System.currentTimeMillis();
                                        long t = endTime-startTime;
                                        tp = new TouchPad(selectedSwimmer.get(index), t, true);
                                        sc.addTouchPad(tp);
                                        selectedSwimmer.get(index).touchTouchPad(tp);                           
                                        selectedSwimmer.get(index).setTime(t);
                                        selectedtouchpad.get(index).setText("   FINISH");  
                                        dtm2.setValueAt(selectedSwimmer.get(index).getTime(), index, 2);
                                        break;
                                    }
                                    Thread.sleep(200);
                                } catch (InterruptedException ex) {

                                }
                            }

                            }
                        };
                        Thread t = new Thread(r);
                        threadArray.add(t);
                    }
                for(Thread t : threadArray){
                    t.start();
                }
                
               
              
                isStartClicked = true;
        }else{
            JOptionPane.showMessageDialog(rootPane, "Fist select five swimmers");
        }        
       
    }//GEN-LAST:event_startMouseClicked
